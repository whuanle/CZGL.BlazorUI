namespace UIBlazor.UIEnum
{
    /// <summary>
    /// 表示 10 种基本颜色，大多数元素和组件可以使用此枚举来设置颜色
    /// </summary>
    public enum UIEColor
    {
        /// <summary>
        /// 无效果
        /// </summary>
        None = 0,

        /// <summary>
        /// 白色
        /// </summary>
        While = 1,

        /// <summary>
        /// 黑色
        /// </summary>
        Black = 2,

        /// <summary>
        /// 光亮 white-ter
        /// </summary>
        Light = 3,

        /// <summary>
        /// 暗黑 grey-darker
        /// </summary>
        Dark = 4,

        /// <summary>
        /// 绿青色 turquoise
        /// </summary>
        Primary = 5,

        /// <summary>
        /// 蓝色 blue
        /// </summary>
        Link = 6,

        /// <summary>
        /// 青色 cyan
        /// </summary>
        Info = 7,

        /// <summary>
        /// 绿色 green
        /// </summary>
        Success = 8,

        /// <summary>
        /// 黄色 yellow
        /// </summary>
        Warning = 9,

        /// <summary>
        /// 红色 red
        /// </summary>
        Danger = 10
    }
}
