namespace UIBlazor.UIEnum
{
    /// <summary>
    /// 辅助类型，设置元素的浮动
    /// </summary>
    public enum UIEFloat
    {
        /// <summary>
        /// 无效果
        /// </summary>
        None = 0,

        /// <summary>
        /// 重置浮动元素的子元素
        /// </summary>
        Fix = 1,

        /// <summary>
        /// 元素向左浮动
        /// </summary>
        Left = 2,

        /// <summary>
        /// 元素向右移动
        /// </summary>
        Right = 3
    }
}
